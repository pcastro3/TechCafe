$(document).ready(function () {

  $("#signup").click(function functionName() {
    $(".first").slideUp("slow", function functionName() {
      $(".second").slideDown("slow");
    })
  })

  $("#signin").click(function functionName() {
    $(".second").slideUp("slow", function functionName() {
      $(".first").slideDown("slow");
    })
  })

})
